第三步：加速VPS服务器（谷歌BBR算法已经开源，各发行版大多已经安装了BBR，这步可省略。）
【谷歌BBR加速教程】
yum -y install wget
wget --no-check-certificate https://raw.githubusercontent.com/HyoukaChitandaEru/BBR/master/bbr.sh
chmod +x bbr.sh
./bbr.sh

把上面整个代码复制后粘贴进去，不动的时候按回车，然后耐心等待，最后重启vps服务器即可。
复制并粘贴代码后，按回车键确认
如提示，按任意键继续部署，等待3～6分钟
 
最后输入y重启服务器，如果输入y提示command not found ，接着输入reboot来重启服务器，确保加速生效，bbr加速脚本是开机自动启动，装一次就可以了。
服务器重启成功并重新连接服务器后，输入命令lsmod | grep bbr 如果出现tcp_bbr字样表示bbr已安装并启动成功。
 
